/**
 * Fragment where the game is played
 */
class GameFragment : Fragment() {

    private lateinit var viewModel: GameViewModel


    private lateinit var binding: GameFragmentBinding
    override fun onCreateView(inflater: LayoutInflater, container: ViewGroup?,
                              savedInstanceState: Bundle?): View? {

        // Inflate view and obtain an instance of the binding class
        binding = DataBindingUtil.inflate(
            inflater,
            R.layout.game_fragment,
            container,
            false
        )
        //BINDING THE VIEWMODEL TO THE VIEW DIRECTLY (?)
        binding.lifecycleOwner = this

        // Get the viewmodel
        viewModel = ViewModelProvider(this).get(GameViewModel::class.java)

        // Set the viewmodel for databinding - this allows the bound layout access to all of the
        // data in the VieWModel
        binding.gameViewModel = viewModel

        /** Setting up LiveData observation relationship **/

       //INSTEAD OF OBSERVERS WE BIND THE VIEWS DIRECTLY TO VIEW MODEL AND LIVE DATA:
        //android:text="@{@string/score_formate(gameViewModel.score)}" <- BINDS SCORE LIVE DATA
        //android:text="@{@string/quote_format(gameViewModel.word)}" <- BINDS WORD LIVE DATA



        viewModel.buzzType.observe(viewLifecycleOwner, Observer { buzz ->
                if(buzz != GameViewModel.BuzzType.NO_BUZZ){
                    buzz(buzz.pattern)
                    viewModel.onBuzzComplete()
                }
        })





        // Sets up event listening to navigate the player when the game is finished
        viewModel.eventGameFinish.observe(viewLifecycleOwner, Observer { isFinished ->
            if (isFinished) {
                val currentScore = viewModel.score.value ?: 0
                val action = GameFragmentDirections.actionGameToScore(currentScore)
                findNavController(this).navigate(action)
                viewModel.onGameFinishComplete()
            }
        })

        return binding.root

    }

    //This goes here because it uses the activity to get a system service,
    // so it does not belong in the GameViewModel:
    private fun buzz(pattern: LongArray) {
        val buzzer = activity?.getSystemService<Vibrator>()

        buzzer?.let {
            if (Build.VERSION.SDK_INT >= Build.VERSION_CODES.O) {
                buzzer.vibrate(VibrationEffect.createWaveform(pattern, -1))
            } else {
                //deprecated in API 26
                buzzer.vibrate(pattern, -1)
            }
        }
    }




}
